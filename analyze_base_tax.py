"""
1,000만원 기준점 값 분석: 두란 계산기 vs 간이세액표
"""

print("=" * 100)
print("🔍 1,000만원 기준점 세액 비교 (33페이지)")
print("=" * 100)
print()

# 두란 계산기의 1,000만원 기준점 (제공된 코드)
duran_tax_at_10m = {
    1: 1507400, 2: 1431570, 3: 1200840, 4: 1170840, 5: 1140840,
    6: 1110840, 7: 1080840, 8: 1050840, 9: 1020840, 10: 990840, 11: 960840
}

# 현재 Git 구현의 간이세액표 1,000만원 값
git_tax_at_10m = {
    1: 890000, 2: 770000, 3: 600000, 4: 510000, 5: 440000,
    6: 380000, 7: 335000, 8: 295000, 9: 260000, 10: 225000, 11: 195000
}

print(f"{'가족수':<10} {'두란 계산기':<20} {'Git(간이세액표)':<20} {'차이':<20} {'차이율'}")
print("-" * 100)

for family in range(1, 12):
    duran_val = duran_tax_at_10m[family]
    git_val = git_tax_at_10m[family]
    diff = duran_val - git_val
    diff_pct = (diff / git_val * 100) if git_val > 0 else 0
    
    print(f"{family}명{'':<7} {duran_val:>15,}원  {git_val:>15,}원  {diff:>15,}원  {diff_pct:>+8.1f}%")

print()
print("=" * 100)
print("📊 핵심 발견")
print("=" * 100)
print()
