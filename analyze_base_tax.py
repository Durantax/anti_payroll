print("   - 가족 1명: 1,507,400원 (두란) vs 890,000원 (간이세액표) = 617,400원 차이 (+69.4%)")
print("   - 가족 3명: 1,200,840원 (두란) vs 600,000원 (간이세액표) = 600,840원 차이 (+100.1%)")
print()
